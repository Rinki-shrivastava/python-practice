# Float variables
num1 = 2
num2 = 10

# Basic Arithemetic
result1 = num1 + num2
print("Addition is", result1)

result2 = num1 - num2
print("Substraction is", result2)

result3 = num1 * num2
print("Multiplication is", result3)

result4 = num1 / num2
print("Division is", result4)

# Rounding
result5 = round(3.14159265359, 2)           # Rounds to 2 decimal places
print("Rounded", result5)
